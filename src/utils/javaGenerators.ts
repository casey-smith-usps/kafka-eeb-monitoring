// ─── InboundRecord.java ───────────────────────────────────────────────────────

export function generateInboundRecord(input: GeneratorInput): string {
  const name = toPascalCase(input.datasetName);
  const pkg = input.basePackage;
  const fields = input.inboundFields.filter(f => f.fieldName.trim());

  const fieldDecls = fields
    .map(f => `    private ${toJavaType(f.avroType, f.nullable)} ${f.fieldName};`)
    .join('\n');

  const getterSetters = fields
    .map(f => {
      const jt = toJavaType(f.avroType, f.nullable);
      const cap = f.fieldName.charAt(0).toUpperCase() + f.fieldName.slice(1);
      return `
    public ${jt} get${cap}() { return ${f.fieldName}; }
    public void set${cap}(${jt} ${f.fieldName}) { this.${f.fieldName} = ${f.fieldName}; }`;
    })
    .join('\n');

  return `package ${pkg}.generated;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Inbound JSON record from topic: ${input.sourceTopic}
 * Generated by EEB ICD Builder.
 */
@JsonIgnoreProperties(ignoreUnknown = true)
public class ${name}InboundRecord {

${fieldDecls}

${getterSetters}
}
`;
}

