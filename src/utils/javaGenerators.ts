// ─── CommonUtil.java ──────────────────────────────────────────────────────────

export function generateCommonUtil(input: GeneratorInput): string {
  const name = toPascalCase(input.datasetName);
  const pkg = input.basePackage;

  return `package ${pkg}.service;

import ${pkg}.config.${name}Config;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.text.ParseException;
import java.text.SimpleDateFormat;
import java.util.Arrays;
import java.util.Date;
import java.util.List;

/**
 * Utility methods for timestamp normalisation, validation, and null-safe coercion.
 */
public class ${name}CommonUtil {

    private static final Logger log = LoggerFactory.getLogger(${name}CommonUtil.class);

    private static final List<String> CANDIDATE_FORMATS = Arrays.asList(
            ${name}Config.ORACLE_DATETIME_FORMAT,
            "yyyy-MM-dd'T'HH:mm:ss.SSS'Z'",
            "yyyy-MM-dd'T'HH:mm:ssXXX",
            "yyyy-MM-dd HH:mm:ss",
            "MM/dd/yyyy HH:mm:ss",
            "yyyyMMddHHmmss"
    );

    /**
     * Normalise any recognised datetime string to the standard output format.
     * Returns the raw value unchanged if no format matches.
     * Accepts CharSequence so callers can pass Avro-generated fields directly
     * without an explicit .toString() cast.
     */
    public static String getStdDtm(CharSequence rawValue) {
        if (rawValue == null || rawValue.toString().isBlank()) return null;
        String raw = rawValue.toString();
        for (String fmt : CANDIDATE_FORMATS) {
            try {
                SimpleDateFormat sdf = new SimpleDateFormat(fmt);
                sdf.setLenient(false);
                Date parsed = sdf.parse(raw.trim());
                return new SimpleDateFormat(${name}Config.STD_DATETIME_FORMAT).format(parsed);
            } catch (ParseException ignored) { /* try next format */ }
        }
        log.warn("getStdDtm: unrecognised datetime format for value [{}]", raw);
        return raw;
    }

    /** Basic scan-datetime validity check (non-null and parseable). */
    public static boolean isValidScanDtm(CharSequence value) {
        return getStdDtm(value) != null;
    }

    /**
     * Returns the value unchanged, or null if blank/null.
     * Accepts CharSequence so Avro string fields can be passed without casting.
     */
    public static String nullIfBlank(CharSequence value) {
        if (value == null) return null;
        String s = value.toString();
        return s.isBlank() ? null : s.trim();
    }

    /**
     * Returns the first non-blank value from the provided candidates, or null.
     * All parameters are String so PMD varargs type checks pass cleanly.
     * Cast Avro CharSequence fields with .toString() before calling this method.
     */
    public static String firstPresent(String... candidates) {
        for (String s : candidates) {
            if (s != null && !s.isBlank()) return s.trim();
        }
        return null;
    }
}
`;
}

// ─── MappingService.java ──────────────────────────────────────────────────────

export function generateMappingService(input: GeneratorInput): string {
  const name = toPascalCase(input.datasetName);
  const pkg = input.basePackage;
  const ob = input.outboundSchema;
  const outFields = ob.fields.filter(f => f.fieldName.trim());

  const mappingLines = outFields.map(f => {
    const setter = toSetter(f.fieldName);
    if (!f.sourceField) {
      return `        // DERIVED: outbound.${setter}(...) — populate from system/config`;
    }
    const getter = toGetter(f.sourceField);
    const isTimestamp =
      f.avroType === 'long' ||
      f.fieldName.toLowerCase().includes('dtm') ||
      f.fieldName.toLowerCase().includes('timestamp') ||
      f.fieldName.toLowerCase().includes('date');

    if (isTimestamp && f.avroType !== 'long') {
      // CharSequence field — pass directly; getStdDtm accepts CharSequence
      return `        outbound.${setter}(${name}CommonUtil.getStdDtm(inbound.${getter}));`;
    }
    if (isTimestamp && f.avroType === 'long') {
      // primitive long — cannot be null, cast directly
      return `        outbound.${setter}((int) inbound.${getter});`;
    }
    if (f.nullable) {
      // nullIfBlank accepts CharSequence — no .toString() needed
      return `        outbound.${setter}(${name}CommonUtil.nullIfBlank(inbound.${getter}));`;
    }
    // String Avro field (stringType=String in BOM) — direct assignment is safe
    return `        outbound.${setter}(String.valueOf(inbound.${getter}));`;
  }).join('\n');

  const outboundClass = toPascalCase(ob.topicName.split('.').pop() ?? 'OutboundRecord');

  return `package ${pkg}.service;

import ${pkg}.config.${name}Config;
import ${pkg}.generated.${name}InboundRecord;
import org.springframework.stereotype.Service;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * Maps a validated ${name}InboundRecord to the outbound Avro model.
 *
 * Topic: ${input.sourceTopic} → ${ob.topicName}
 */
@Service
public class ${name}MappingService {

    private static final Logger log = LoggerFactory.getLogger(${name}MappingService.class);

    /**
     * Perform the field-level mapping.
     *
     * @param processorResult mapping context (inbound record + metadata)
     * @return populated outbound record — caller must call .build()
     * @throws IllegalArgumentException if a required field is missing or invalid
     */
    public ${outboundClass}.Builder map(${name}MappingContext processorResult) {
        // PMD S1481: reference the parameter so it is not flagged as unused
        java.util.Objects.requireNonNull(processorResult);
        ${name}InboundRecord inbound = processorResult.getInboundRecord();

        ${outboundClass}.Builder outbound = ${outboundClass}.newBuilder();

        // ── Appln envelope (system-generated) ────────────────────────────
        outbound.setEvSrc(${name}Config.DEFAULT_EVENT_SOURCE);
        outbound.setEvTyp(${name}Config.DEFAULT_EVENT_TYPE);
        outbound.setArrDtm(${name}CommonUtil.getStdDtm(
                new java.text.SimpleDateFormat("MM-dd-yyyy HH:mm:ss.SSS")
                        .format(new java.util.Date())));

        // ── Field mappings ────────────────────────────────────────────────
${mappingLines}

        log.debug("Mapped record for partition={} offset={}",
                processorResult.getPartition(), processorResult.getSourceOffset());

        // PMD VariableCanBeInlined: return builder directly — no temp variable
        return outbound;
    }
}
`;
}

// ─── BatchProcessor.java ──────────────────────────────────────────────────────

export function generateBatchProcessor(input: GeneratorInput): string {
  const name = toPascalCase(input.datasetName);
  const pkg = input.basePackage;
  const ob = input.outboundSchema;
  const outboundClass = toPascalCase(ob.topicName.split('.').pop() ?? 'OutboundRecord');

  return `package ${pkg}.processor;

import ${pkg}.config.${name}Config;
import ${pkg}.generated.${name}InboundRecord;
import ${pkg}.service.${name}MappingContext;
import ${pkg}.service.${name}MappingService;
import com.fasterxml.jackson.databind.ObjectMapper;
import org.apache.kafka.clients.consumer.ConsumerRecord;
import org.apache.kafka.clients.producer.ProducerRecord;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.kafka.annotation.KafkaListener;
import org.springframework.kafka.core.KafkaTemplate;
import org.springframework.stereotype.Component;

import java.util.List;

/**
 * Kafka batch listener for ${input.sourceTopic}.
 * Processes records in batches, maps each to ${ob.topicName}, and produces.
 */
@Component
public class ${name}BatchProcessor {

    private static final Logger log = LoggerFactory.getLogger(${name}BatchProcessor.class);

    private final ${name}MappingService mappingService;
    private final KafkaTemplate<Object, Object> kafkaTemplate;
    private final ObjectMapper objectMapper;

    @Autowired
    public ${name}BatchProcessor(${name}MappingService mappingService,
                                  KafkaTemplate<Object, Object> kafkaTemplate,
                                  ObjectMapper objectMapper) {
        this.mappingService = mappingService;
        this.kafkaTemplate  = kafkaTemplate;
        this.objectMapper   = objectMapper;
    }

    @KafkaListener(
        topics    = ${name}Config.INBOUND_TOPIC,
        groupId   = "\${spring.kafka.consumer.group-id}",
        batch     = "true",
        containerFactory = "batchKafkaListenerContainerFactory"
    )
    public void processBatch(List<ConsumerRecord<String, String>> records) {
        log.info("Received batch of {} records from {}", records.size(), ${name}Config.INBOUND_TOPIC);

        int success = 0;
        int failure = 0;

        // 'record' is a restricted keyword in Java 16+; use 'consumerRecord' instead
        for (ConsumerRecord<String, String> consumerRecord : records) {
            try {
                ${name}InboundRecord inbound = objectMapper.readValue(
                        consumerRecord.value(), ${name}InboundRecord.class);

                ${name}MappingContext ctx = new ${name}MappingContext(
                        inbound,
                        String.valueOf(consumerRecord.offset()),
                        consumerRecord.partition());

                // PMD VariableCanBeInlined: send the built value directly
                kafkaTemplate.send(new ProducerRecord<>(
                        ${name}Config.OUTBOUND_TOPIC,
                        consumerRecord.key(),
                        mappingService.map(ctx).build()));
                success++;

            } catch (Exception ex) {
                log.error("Failed to process record partition={} offset={}: {}",
                        consumerRecord.partition(), consumerRecord.offset(), ex.getMessage(), ex);
                failure++;
            }
        }

        log.info("Batch complete — success={} failure={}", success, failure);
    }
}
`;
}

