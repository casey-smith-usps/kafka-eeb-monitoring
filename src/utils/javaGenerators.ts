// ─── Avro helpers (duplicated from AvroSchemaBuilder to keep utils self-contained)

function buildAvsc(namespace: string, recordName: string, fields: IcdField[]): object {
  return {
    type: 'record',
    name: recordName,
    namespace,
    fields: fields
      .filter(f => f.fieldName.trim() !== '')
      .map(f => {
        if (f.nullable) {
          return { name: f.fieldName, type: ['null', f.avroType], default: null, doc: f.description || undefined };
        }
        return { name: f.fieldName, type: f.avroType, doc: f.description || undefined };
      }),
  };
}

function buildDataSpecCsv(input: GeneratorInput): string {
  const rows = ['sourceField,sourceType,targetField,targetType,nullable,mappingType,transformNotes,isPii,description'];
  const inMap = new Map(input.inboundFields.map(f => [f.fieldName, f]));
  for (const f of input.outboundSchema.fields) {
    if (!f.fieldName.trim()) continue;
    const src = f.sourceField.trim();
    const inF = inMap.get(src);
    const mt = src ? (src === f.fieldName ? 'direct' : 'rename') : 'derived';
    rows.push([src, inF?.avroType ?? '', f.fieldName, f.avroType, f.nullable, mt, '', f.isPii, `"${f.description.replace(/"/g, '""')}"`].join(','));
  }
  return rows.join('\n');
}
