// Java source file generators for the EEB ingest template

export interface IcdField {
  id: string;
  fieldName: string;
  avroType: string;
  nullable: boolean;
  isPii: boolean;
  description: string;
  sourceField: string;
  defaultValue: string;
  order: number;
}

export interface OutboundSchema {
  id: string;
  topicName: string;
  description: string;
  fields: IcdField[];
  keyFields: IcdField[];
}

export interface GeneratorInput {
  projectName: string;
  datasetName: string;
  basePackage: string;
  sourceTopic: string;
  inboundFields: IcdField[];
  inboundKeyFields: IcdField[];
  outboundSchema: OutboundSchema;
}

// e.g. "MID_OWNER_HISTORY" → "MidOwnerHistory"
function toPascalCase(name: string): string {
  return name
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim()
    .split(' ')
    .map(w => w.charAt(0).toUpperCase() + w.slice(1).toLowerCase())
    .join('');
}

function toJavaType(avroType: string, nullable: boolean): string {
  const map: Record<string, string> = {
    string: 'String',
    int: 'Integer',
    long: 'Long',
    float: 'Float',
    double: 'Double',
    boolean: 'Boolean',
    bytes: 'byte[]',
  };
  return map[avroType] ?? 'String';
}

function toGetter(fieldName: string): string {
  return `get${fieldName.charAt(0).toUpperCase()}${fieldName.slice(1)}()`;
}

function toSetter(fieldName: string): string {
  return `set${fieldName.charAt(0).toUpperCase()}${fieldName.slice(1)}`;
}

